#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;

	i = 0;
	if (s1[i] == '\0' && s2[i] != '\0')
		return (NULL);
	while (s1[i] == s2[i] && s1[i] != '\0')
		i++;
	if (s2[i] == '\0')
		return ((char*)s1);
	return (ft_strstr(s1 + 1, s2));
}
