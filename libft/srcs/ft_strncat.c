#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t t;

	i = ft_strlen(s1);
	t = 0;
	while (s2[t] && t < n)
		s1[i++] = s2[t++];
	s1[i] = '\0';
	return (s1);
}
